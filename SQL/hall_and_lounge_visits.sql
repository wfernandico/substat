	TRADESHOW_LOUNGEHRCOM LOUNGES
ON
	LOUNGES.ID = EVENTS.TARGET_ID
WHERE
	LOUNGES.SHOW_ID = 1629319409551
	AND YYYYMMDD >= '20211114';